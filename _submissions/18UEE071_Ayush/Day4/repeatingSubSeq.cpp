int anytwo(string A)
{
    string B = A;
    // reverse(B.begin(), B.end());
    int l = A.size();
    vector<vector<int>> dp(l + 1, vector<int>(l + 1, 0));

    for (int i = 1; i <= l; i++)
    {
        for (int j = 1; j <= l; j++)
        {
            if (i == j)
                continue;
            if (A[i - 1] == B[j - 1])
            {
                dp[i][j] = dp[i - 1][j - 1] + 1;
            }
            else
            {
                dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
            }
        }
    }
    return *max_element(dp[l].begin(), dp[l].end()) >= 2;
}

